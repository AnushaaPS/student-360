/************************************************
  GENERIC EXCEL EXPORT FUNCTION
************************************************/

function exportToExcel(data,fileName){

    const worksheet = XLSX.utils.json_to_sheet(
        data
    );

    const workbook = XLSX.utils.book_new();

    XLSX.utils.book_append_sheet(

        workbook,

        worksheet,

        "Analytics"

    );

    XLSX.writeFile(

        workbook,

        `${fileName}.xlsx`

    );

}

