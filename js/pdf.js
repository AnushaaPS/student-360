/************************************************
  GENERATE STUDENT PDF REPORT
************************************************/

async function generateStudentPDF(student){

    const { jsPDF } = window.jspdf;

    const doc = new jsPDF();


    /********************************************
      BUILD ANALYTICS
    ********************************************/

    const analytics =
        buildStudentAnalytics(student);

    const indices =
        analytics.indices;

    const recommendations =
        getRecommendations(indices);


    /********************************************
      TITLE
    ********************************************/

    doc.setFontSize(20);

    doc.text(
        "360° Student Analytics Report",
        20,
        20
    );


    /********************************************
      STUDENT DETAILS
    ********************************************/

    doc.setFontSize(12);

    doc.text(
        `Name: ${student.Name}`,
        20,
        40
    );

    doc.text(
        `Roll No: ${student.RollNo}`,
        20,
        50
    );

    doc.text(
        `Department: ${student.Dept}`,
        20,
        60
    );

    doc.text(
        `Year: ${student.Year}`,
        20,
        70
    );

    doc.text(
        `Section: ${student.Section}`,
        20,
        80
    );


    /********************************************
      KPI SCORES
    ********************************************/

    doc.setFontSize(16);

    doc.text(
        "Performance Metrics",
        20,
        100
    );

    doc.setFontSize(12);

    doc.text(
        `360° Score: ${analytics.score360.toFixed(2)}`,
        20,
        115
    );

    doc.text(
        `Placement Readiness: ${analytics.placement.toFixed(2)}%`,
        20,
        125
    );

    doc.text(
        `Risk Level: ${analytics.riskLevel}`,
        20,
        135
    );

    doc.text(
        `CGPA Index: ${indices.CGPA.toFixed(2)}`,
        20,
        145
    );


    /********************************************
      INDICES
    ********************************************/

    doc.setFontSize(16);

    doc.text(
        "360° Performance Indices",
        20,
        165
    );

    doc.setFontSize(12);

    let y = 180;

    Object.keys(indices).forEach(key => {

        doc.text(

            `${key}: ${indices[key].toFixed(2)}`,

            20,

            y

        );

        y += 10;

    });


    /********************************************
      RECOMMENDATIONS
    ********************************************/

    y += 10;

    doc.setFontSize(16);

    doc.text(
        "Recommendations",
        20,
        y
    );

    y += 15;

    doc.setFontSize(12);

    recommendations.forEach(rec => {

        doc.text(

            `• ${rec}`,

            25,

            y

        );

        y += 10;

    });


    /********************************************
      FOOTER
    ********************************************/

    doc.setFontSize(10);

    doc.text(

        "Generated by Advanced 360° Student Analytics System",

        20,

        280

    );


    /********************************************
      SAVE PDF
    ********************************************/

    doc.save(

        `${student.RollNo}_Analytics_Report.pdf`

    );

}



