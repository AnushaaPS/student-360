/************************************************
  DOWNLOAD PLACEMENT REPORT XLSX
************************************************/

function downloadPlacementReportXLSX(){

    let data = [];

    CLASS_STUDENTS.forEach(student => {

        const analytics =
            buildStudentAnalytics(student);

        data.push({

            "Roll No" :
                student.RollNo,

            "Name" :
                student.Name,

            "Department" :
                student.Dept,

            "360 Score" :
                analytics.score360.toFixed(2),

            "Placement Readiness %" :
                analytics.placement.toFixed(2),

            "Category" :

                analytics.placement >= 85

                ?

                "Industry Ready"

                :

                analytics.placement >= 70

                ?

                "Placement Ready"

                :

                "Needs Improvement"

        });

    });


    exportToExcel(

        data,

        "Placement_Readiness_Report"

    );

}



/************************************************
  DOWNLOAD GROWTH ANALYSIS XLSX
************************************************/

function downloadGrowthReportXLSX(){

    let data = [];

    CLASS_STUDENTS.forEach(student => {

        const analytics =
            buildStudentAnalytics(student);

        let growth = "Stable";

        if(analytics.indices.Growth > 0){

            growth = "Improving";

        }
        else if(
            analytics.indices.Growth < 0
        ){

            growth = "Declining";

        }

        data.push({

            "Roll No" :
                student.RollNo,

            "Name" :
                student.Name,

            "Department" :
                student.Dept,

            "Growth Score" :
                analytics.indices.Growth.toFixed(2),

            "Growth Status" :
                growth

        });

    });


    exportToExcel(

        data,

        "Growth_Analysis_Report"

    );

}



